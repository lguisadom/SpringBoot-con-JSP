package com.lagm.util;

public class Persona {
	private String nombre;
	private int edad;

	public final String getNombre() {
		return nombre;
	}

	public final void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public final int getEdad() {
		return edad;
	}

	public final void setEdad(int edad) {
		this.edad = edad;
	}

}
